var tabLang = gLanguage["en"];

function SetMapLanguage(jsLang)
{
	if(jsLang)
	{
		try
		{
			if(jsLang.hl && jsLang.hl != tabLang.hl) {
				tabLang = gLanguage[jsLang.hl];
			}
		} catch(ex)
		{
			Trace('SetMapLanguage', ex);
		}

		tabLang.tthome = jsLang.tthome||null;
		tabLang.nvgfresh = jsLang.nvgfresh||null;
		tabLang.navgate = jsLang.navgate||null;
		tabLang.miomore = jsLang.miomore||null;
		tabLang.garmin = jsLang.garmin||null;
	}
}
